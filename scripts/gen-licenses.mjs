#!/usr/bin/env node
// Generate THIRD-PARTY-LICENSES.md + .csv from license-checker (JS) and
// cargo metadata (Rust). Run from repo root: `node scripts/gen-licenses.mjs`.
//
// Outputs:
//   THIRD-PARTY-LICENSES.md   — human-readable summary
//   THIRD-PARTY-LICENSES.csv  — full per-dep table (CSV: name, version, license, ecosystem, repo)
//
// Designed for "lean core" — the JS list is large (~445 deps) and the
// cargo list is large (~525 deps), so the markdown summary is the
// readable surface and the CSV is the audit table. The script is
// idempotent: rerun any time deps change and `git diff` the output.
import { execSync } from 'node:child_process';
import { writeFileSync, readFileSync, mkdtempSync, rmSync } from 'node:fs';
import { tmpdir } from 'node:os';
import { join } from 'node:path';

const ROOT = process.cwd();
const csv = (s) => `"${String(s).replace(/"/g, '""')}"`;

function run(cmd) {
  try { return execSync(cmd, { encoding: 'utf-8', stdio: ['pipe', 'pipe', 'pipe'] }).trim(); }
  catch (e) { console.error(`cmd failed: ${cmd}\n${e.stderr || e.message}`); process.exit(1); }
}

// ── JS deps via license-checker ────────────────────────────────────────────
const jsJson = run('npx --yes license-checker --production --json 2>/dev/null');
const js = JSON.parse(jsJson);

const jsRows = Object.entries(js).map(([name, info]) => {
  // license-checker emits names like "papaparse@5.5.3"
  const at = name.lastIndexOf('@');
  const pkg = at > 0 ? name.slice(0, at) : name;
  const ver = at > 0 ? name.slice(at + 1) : '';
  return {
    name: pkg,
    version: ver,
    license: info.licenses || 'UNKNOWN',
    repository: info.repository || '',
  };
});

// ── Cargo deps via cargo metadata ──────────────────────────────────────────
// cargo metadata output is large (hundreds of packages); write to a
// temp file and read, since piping straight into the shell hits
// ENOBUFS in some environments.
const tmpDir = mkdtempSync(join(tmpdir(), 'mdreader-lic-'));
const tmpFile = join(tmpDir, 'cargo-meta.json');
try {
  run(`cd ${ROOT}/src-tauri && cargo metadata --format-version=1 --offline > ${tmpFile} 2>/dev/null || cargo metadata --format-version=1 > ${tmpFile} 2>/dev/null`);
  const cargo = JSON.parse(readFileSync(tmpFile, 'utf-8'));
  var cargoRows = cargo.packages
    .filter((p) => p.source && p.source.startsWith('registry+')) // skip path/git/local
    .map((p) => ({
      name: p.name,
      version: p.version,
      license: p.license || 'UNKNOWN',
      repository: p.repository || '',
    }));
} finally {
  try { rmSync(tmpDir, { recursive: true, force: true }); } catch {}
}

// ── License distribution ───────────────────────────────────────────────────
function tally(rows) {
  const m = new Map();
  for (const r of rows) {
    const k = r.license;
    m.set(k, (m.get(k) || 0) + 1);
  }
  return [...m.entries()].sort((a, b) => b[1] - a[1]);
}
const jsTally = tally(jsRows);
const cargoTally = tally(cargoRows);

// Notable license flags — any dep not in the permissive set gets called
// out so the README can link to the explicit attribution.
const NOTABLE = new Set(['EPL-2.0', 'MPL-2.0', 'LGPL-2.1-or-later', 'LGPL-3.0-or-later',
  'GPL-2.0-or-later', 'GPL-3.0-or-later', 'AGPL-3.0-or-later',
  'CC-BY-4.0', 'CC-BY-SA-4.0', 'BSL-1.0']);
function notable(rows) {
  return rows.filter((r) => {
    for (const tok of r.license.split(/[ ()\/]+/)) {
      if (NOTABLE.has(tok)) return true;
    }
    return false;
  });
}
const jsNotable = notable(jsRows);
const cargoNotable = notable(cargoRows);

// ── Write CSV ──────────────────────────────────────────────────────────────
const csvLines = ['ecosystem,name,version,license,repository'];
for (const r of jsRows) csvLines.push(['npm', r.name, r.version, r.license, r.repository].map(csv).join(','));
for (const r of cargoRows) csvLines.push(['cargo', r.name, r.version, r.license, r.repository].map(csv).join(','));
writeFileSync(`${ROOT}/THIRD-PARTY-LICENSES.csv`, csvLines.join('\n') + '\n');

// ── Write markdown summary ─────────────────────────────────────────────────
function pct(n, d) { return ((n / d) * 100).toFixed(1); }
function tallyTable(tallyRows, total) {
  return tallyRows.map(([lic, n]) => `| ${lic} | ${n} | ${pct(n, total)}% |`).join('\n');
}

const md = `# Third-Party Licenses

MD Reader (MIT) is built on top of open-source dependencies from
the npm and Cargo ecosystems. This file is the human-readable
summary; the per-dependency table is in
[THIRD-PARTY-LICENSES.csv](./THIRD-PARTY-LICENSES.csv).

Generated by \`scripts/gen-licenses.mjs\` — rerun any time deps
change and \`git diff\` the output.

---

## Summary

| Ecosystem | Total deps | Permissive (MIT/Apache/BSD/ISC/0BSD/Unlicense/Zlib/CC0/MIT-0/BSL) | Notable |
|---|---:|---:|---:|
| npm (production) | ${jsRows.length} | ${jsRows.length - jsNotable.length} | ${jsNotable.length} |
| Cargo | ${cargoRows.length} | ${cargoRows.length - cargoNotable.length} | ${cargoNotable.length} |

## npm — license distribution (top 20)

| License | Count | % |
|---|---:|---:|
${tallyTable(jsTally.slice(0, 20), jsRows.length)}

${jsTally.length > 20 ? `\n…and ${jsTally.length - 20} more (see CSV).\n` : ''}
## Cargo — license distribution (top 20)

| License | Count | % |
|---|---:|---:|
${tallyTable(cargoTally.slice(0, 20), cargoRows.length)}

${cargoTally.length > 20 ? `\n…and ${cargoTally.length - 20} more (see CSV).\n` : ''}
## Notable licenses

The licenses below are not in the standard "permissive" set and
warrant explicit attribution or a compliance check before shipping
a binary distribution. The full list is in the CSV.

${jsNotable.length ? `### npm (${jsNotable.length})\n\n| Package | Version | License |\n|---|---|---|\n${jsNotable.map((r) => `| \`${r.name}\` | ${r.version} | ${r.license} |`).join('\n')}\n` : ''}
${cargoNotable.length ? `### Cargo (${cargoNotable.length})\n\n| Crate | Version | License |\n|---|---|---|\n${cargoNotable.map((r) => `| \`${r.name}\` | ${r.version} | ${r.license} |`).join('\n')}\n` : ''}
## One transitive dep under EPL-2.0

\`elkjs@0.9.3\` (npm) — the layout engine mermaid uses for some
diagram types. EPL-2.0 is a weak copyleft: source-level
modifications to elkjs itself must be released, but merely
distributing MD Reader (which links elkjs via mermaid) is fine.
The dep is loaded lazily, so a chapter without a mermaid diagram
never pulls it in.

---

_Generated: ${new Date().toISOString().slice(0, 10)} · ${jsRows.length} npm + ${cargoRows.length} cargo deps_
`;

writeFileSync(`${ROOT}/THIRD-PARTY-LICENSES.md`, md);

console.log(`Wrote THIRD-PARTY-LICENSES.md + .csv`);
console.log(`  npm: ${jsRows.length} deps, ${jsNotable.length} notable`);
console.log(`  cargo: ${cargoRows.length} deps, ${cargoNotable.length} notable`);
